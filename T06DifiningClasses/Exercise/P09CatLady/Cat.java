package T06DifiningClasses.Exercise.P09CatLady;

public class Cat {
    private String breed;
    private String name;
   public Cat(String breed, String name) {
       this.breed = breed;
       this.name = name;
   }

   public String getBreed() {
       return this.breed;
   }

   public String getName() {
       return this.name;
   }

}
